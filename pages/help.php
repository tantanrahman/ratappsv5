 <ol>
 	<li>
 		<b>Upload Data Anggota</b>
 	</li>
 	<ol type="a">
 		<li>
 			Masuk kedalam <i>Menu</i> <b>"Upload Data Anggota"</b>.
 		</li>
 		<li>
 			Pilih file dengan format <strong>.xls</strong> jika sebelumnya Data Anggota sudah ada silakan centang pada bagian "Kosongkan table di database terlebih dahulu". Pilih "Import".
 		</li>
 	</ol>
 	<li>
 		<b>Memasukan Data Yang Hadir</b>
 	</li>
 	<ol type="a">
 		<li>
 			Masuk kedalam <i>Menu</i> <b>"Input Absensi"</b>.
 		</li>
 		<li>
 			Masukan NAK / NAMA / NIK anggota yang akan melakukan Registrasi.
 		</li>
 		<li>
 			Pilih status HADIR / KUASA.
 		</li>
 		<li>
 			Pilih "Tambah Absensi".
 		</li>
 		<li>
 			Nama akan masuk dalam tabel rekap dibawah, jika tidak akan menguasakan langsung pilih "Simpan Absensi".
 		</li>
 	</ol>
 	<li>
 		<b>Memasukan Data Yang Ingin Dikuasakan</b>
 	</li>
 	<ol type="a">
 		<li>
 			Masuk kedalam <i>Menu</i> <b>"Input Absensi"</b>.
 		</li>
 		<li>
 			Masukan NAK / NAMA / NIK anggota yang akan melakukan Registrasi.
 		</li>
 		<li>
 			Pilih status HADIR / KUASA.
 		</li>
 		<li>
 			Pilih "Tambah Absensi".
 		</li>
 		<li>
 			Masukan kembali NAK / NAMA /NIK anggota yang ingin dikuasakan sesuai dengan jumlah yang ingin dikuasakan.
 		</li>
 		<li>
 			Setelah anggota HADIR dan menguasakan masuk ke dalam tabel rekap semuanya pilih "Tambah Absensi".
 		</li>
 	</ol>
 	<li>
 		<b>Mencetak Bukti Data Anggota (Hadir dan Kuasa)</b>
 	</li>
 	<ol type="a">
 		<li>
 			Masuk kedalam <i>Menu</i> <b>"Cetak Bukti Pembayaran"</b>.
 		</li>
 		<li>
 			Dalam kolom "Search" silakan cari anggota yang dimaksud.
 		</li>
 		<li>
 			Setelah Data Anggota didapat jangan lupa untuk pilig tombol cetak di paling kanan dan pilih "Cetak". (Jika anggota tersebut menguasakan anggota lainnya nama anggota yang dikuasakan akan otomatis terjumlah dalam <i>PDF</i> atau hasil cetaknya).
 		</li>
 	</ol>
 	<li>
 		<b>Mencetak Bukti Data Anggota Yang Dikuasakan Tapi Hadir</b>
 	</li>
 	<ol type="a">
 		<li>
 			Harus dipastikan Data Anggota sudah dikuasakan oleh salah satu Anggota.
 		</li>
 		<li>
 			Masuk kedalam <i>Menu</i> <b>"Input Kuasa Yang Hadir"</b>.
 		</li>
 		<li>
 			Pilih salah satu Anggota yang ingin dicetak bukti hadirnya. Pilih di tombol paling kanan dan "Simpan Absensi".
 		</li>
 		<li>
 			Masuk kedalam <i>Menu</i> <b>"Cetak Kupon (Khusus Kuasa Yang Hadir)"</b>.
 		</li>
 		<li>
 			Pilih Anggota yang ingin dicetak. (Dapat dilakukan menggunakan "Search").
 		</li>
 	</ol>
 	<li>
 		<b>Menambahkan Bendahara Yang Bertugas</b>
 	</li>
 	<ol type="a">
 		<li>
 			Pilih menu "Tambah Bendahara" di bagian kanan atas.
 		</li>
 		<li>
 			Ganti nama Bendahara sesuai dengan Anggota yang bertugas. Lalu pilih simpan.
 		</li>
 	</ol>
 	<li>
 		<b>Mencetak Laporan Akhir.</b>
 	</li>
 	<ol type="a">
 		<li>
 			Masuk kedalam <i>Menu</i> <b>"Laporan Kehadiran"</b>.
 		</li>
 		<li>
 			Pilih Tombol Cetak.
 		</li>
 	</ol>
 </ol>